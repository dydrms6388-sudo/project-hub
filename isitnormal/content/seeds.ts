/** 카테고리 순서 (related 계산 시 '다음 카테고리' 참조용) */
const CATEGORY_ORDER: CategorySlug[] = [
  "living", "food", "sleep", "dating", "couple", "family",
  "money", "work", "school", "body", "mind", "quirks",
];

/**
 * related 5개 계산: 같은 카테고리의 다른 시드(최대 4) + 다음 카테고리 시드로 채움.
 * 항상 5개 유효 slug 보장 → 깨진 링크·고아 페이지 0 (F7).
 */
function buildRelated(raw: RawSeed[]): Map<string, string[]> {
  const byCat = new Map<CategorySlug, RawSeed[]>();
  for (const s of raw) {
    (byCat.get(s.categorySlug) ?? byCat.set(s.categorySlug, []).get(s.categorySlug)!).push(s);
  }
  const map = new Map<string, string[]>();
  for (const s of raw) {
    const sameCat = (byCat.get(s.categorySlug) ?? []).filter((o) => o.slug !== s.slug);
    const related = sameCat.map((o) => o.slug);
    let nextIdx = (CATEGORY_ORDER.indexOf(s.categorySlug) + 1) % CATEGORY_ORDER.length;
    while (related.length < 5) {
      const pool = byCat.get(CATEGORY_ORDER[nextIdx]) ?? [];
      for (const o of pool) {
        if (related.length >= 5) break;
        if (o.slug !== s.slug && !related.includes(o.slug)) related.push(o.slug);
      }
      nextIdx = (nextIdx + 1) % CATEGORY_ORDER.length;
    }
    map.set(s.slug, related.slice(0, 5));
  }
  return map;
}

const RELATED = buildRelated(RAW);

